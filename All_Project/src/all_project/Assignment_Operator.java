
package all_project;

public class Assignment_Operator {
    public static void main(String[] args) {
        int x = 4;
        int y = 3;
        
        x+=y;
        System.out.println("X + Y= "+x);
        
        x-=y;
        System.out.println("X - Y= "+x);
        
        x*=y;
        System.out.println("X * Y= "+x);
        
        x/=y;
        System.out.println("X / Y= "+x);
        
        x%=y;
        System.out.println("X % Y= "+x);
    }
}
